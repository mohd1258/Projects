-- Remove Duplicates

--To view the Duplicate rows
--Creating a CTE will help in making temporary chnages to number each duplicate and non-duplicate rows

WITH RowNumCTE AS(
Select *,
	ROW_NUMBER() OVER (
	PARTITION BY ParcelID,
				 PropertyAddress,
				 SalePrice,
				 SaleDate,
				 LegalReference
				 ORDER BY
					UniqueID
					) row_num

From PortfolioProject.dbo.NashvilleHousing
--order by ParcelID
)
Select *
From RowNumCTE
Where row_num > 1
Order by PropertyAddress

-- To delete the duplicate rows
DELETE *
From RowNumCTE
Where row_num > 1
Order by PropertyAddress




